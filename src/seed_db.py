def seed_db():
    db = SessionLocal()
    for act in initial_activities:
        if not db.query(Activity).filter(Activity.name == act["name"]).first():
            activity = Activity(
                name=act["name"],
                description=act["description"],
                schedule=act["schedule"],
                max_participants=act["max_participants"]
            )
            db.add(activity)
            db.commit()
            db.refresh(activity)
            for email in act["participants"]:
                participant = Participant(email=email, activity_id=activity.id)
                db.add(participant)
            db.commit()
    db.close()

if __name__ == "__main__":
    seed_db()
    print("Database seeded.")
